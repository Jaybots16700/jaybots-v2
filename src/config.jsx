export const teamColors = (team, hover, border, justborder) => {
  if (hover) {
    if (border) {
      if (team == 'Jaybots') {
        return ' hover:border-blue-800 '
      } else if (team == 'Bluebirds') {
        return ' hover:border-blue-800 '
      } else if (team == 'Phoenix') {
        return ' hover:border-red-700 '
      }
    }

    if (team == 'Jaybots') {
      return ' hover:bg-blue-600 '
    } else if (team == 'Bluebirds') {
      return ' hover:bg-blue-600 '
    } else if (team == 'Phoenix') {
      return ' hover:bg-red-600 '
    }
  }

  if (border) {
    if (team == 'Jaybots') {
      return ' border-blue-800 bg-blue-700 '
    } else if (team == 'Bluebirds') {
      return ' border-blue-600 bg-blue-500 '
    } else if (team == 'Phoenix') {
      return ' border-red-800 bg-red-700 '
    }
  }

  if (justborder) {
    if (team == 'Jaybots') {
      return ' border-blue-700 '
    } else if (team == 'Bluebirds') {
      return ' border-blue-500 '
    } else if (team == 'Phoenix') {
      return ' border-red-700 '
    }
  }

  if (team == 'Jaybots') {
    return ' bg-blue-700 '
  } else if (team == 'Bluebirds') {
    return ' bg-blue-500 '
  } else if (team == 'Phoenix') {
    return ' bg-red-700 '
  }
}

